package conditionalStatements;

public class MathematicalOperators {

	public static void main(String[] args) {
		
		double a=7;
		double b=4;
		
		System.out.println(a+b);
		
		System.out.println(b-a);
		
		System.out.println(a/b);
		
		System.out.println(a*b);
		
		System.out.println(a%b);

	}

}
